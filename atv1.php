<?php
$valor1 = 10;
$valor2 = 3;

// Adição
$resultado = $valor1 + $valor2;
echo "Adição: " . $resultado . "<br>";

// Subtração
$resultado = $valor1 - $valor2;
echo "Subtração: " . $resultado . "<br>";

// Divisão
$resultado = $valor1 / $valor2;
echo "Divisão: " . $resultado . "<br>";

// Multiplicação
$resultado = $valor1 * $valor2;
echo "Multiplicação: " . $resultado . "<br>";

// Resto da Divisão
$resultado = $valor1 % $valor2;
echo "Resto da Divisão: " . $resultado . "<br>";
?>
